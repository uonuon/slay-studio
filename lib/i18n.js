export function tName(name, lang) {
  if (lang !== "ar" || !name) return name;
  const [fam, size] = name.split(" · ");
  const f = FAMILY_AR[fam] || fam;
  if (!size) return f;
  return f + " · " + (SIZE_AR[size] || size);
}
export function tSize(size, lang) {
  if (lang !== "ar" || !size) return size;
  return SIZE_AR[size] || size;
}
// Chip label for a variant: custom-named styles show their full name (only the
// built-in ones translate); legacy size variants translate via the size table.
export function tVariant(o, lang) {
  return o.group ? tName(o.name, lang) : tSize(o.variant, lang);
}
export function laneLabel(lane, lang, kind = "full") {
  const e = LANE_STR[lane];
  if (!e) return lane;
  return (e[lang] || e.en)[kind];
}

// ---- dates / days --------------------------------------------------------
export function fmtDateL(s, lang) {
  const d = new Date(s + "T00:00:00");
  try {
    return d.toLocaleDateString(lang === "ar" ? "ar-EG" : "en-GB", {
      weekday: "short", day: "numeric", month: "short",
    });
  } catch (e) {
    return s;
  }
}
const DAY_AR = ["أحد", "إثنين", "ثلاثاء", "أربعاء", "خميس", "جمعة", "سبت"];
const DAY_EN = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];
export const dayShort = (dow, lang) => (lang === "ar" ? DAY_AR : DAY_EN)[dow];

// Display a stored 24h "HH:MM" time as 12-hour with AM/PM (ص/م in Arabic).
// Storage stays 24h everywhere; this is display-only.
export function fmtTime(t, lang) {
  if (!t || !/^\d{1,2}:\d{2}$/.test(t)) return t || "";
  let [h, m] = t.split(":").map(Number);
  const am = h < 12;
  let h12 = h % 12; if (h12 === 0) h12 = 12;
  const mm = String(m).padStart(2, "0");
  const suffix = lang === "ar" ? (am ? "ص" : "م") : (am ? "AM" : "PM");
  return `${h12}:${mm} ${suffix}`;
}

// ---- context -------------------------------------------------------------
const LangCtx = createContext({ lang: "ar", setLang: () => {}, t: (k) => k, dir: "rtl" });

export function LangProvider({ children }) {
  const [lang, setLang] = useState("en");

  useEffect(() => {
    const saved = typeof localStorage !== "undefined" && localStorage.getItem("ss_lang");
    if (saved === "en" || saved === "ar") setLang(saved);
  }, []);

  useEffect(() => {
    if (typeof localStorage !== "undefined") localStorage.setItem("ss_lang", lang);
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    }
  }, [lang]);

  const t = (key, params) => {
    let s = (STR[lang] && STR[lang][key]) ?? STR.en[key] ?? key;
    if (params) for (const k in params) s = s.split("{" + k + "}").join(params[k]);
    return s;
  };
  const dir = lang === "ar" ? "rtl" : "ltr";

  return <LangCtx.Provider value={{ lang, setLang, t, dir }}>{children}</LangCtx.Provider>;
}

export const useLang = () => useContext(LangCtx);
